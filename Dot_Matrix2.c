#include <stdio.h>
#include <stdlib.h>
#include <fcntl.h>

#define dot "/dev/dot"

int main()
{
    int dot_d, i;
    unsigned char c[4][8] = {    {0x00,0x66,0xFF,0xFF,0x7E,0x3C,0x18,0x00}, //하트
    {0x00,0x08,0x1C,0x3E,0x7F,0x3E,0x1C,0x08}, //스페이드
    {0x18,0x00,0x18,0x3C,0x3C,0x18,0x00,0x00}, //클로버
    {0x00,0x08,0x1C,0x3E,0x1C,0x08,0x00,0x00} //다이아몬드
                };
    if((dot_d = open(dot, O_RDWR)) < 0)
    {
        printf("Can't Open\n");
        exit(0);
    }

    for(i=0;i<4;i++)
    {
        write(dot_d,&c[i],sizeof(c[i]));
        sleep(1);
    }
    close(dot_d);

    return 0;
}